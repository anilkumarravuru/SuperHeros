class Character:
    def __init__(self, name='', intelligence=0, strength=0, speed=0, durability=0, power=0, combat=0, height=0, weight=0):
        self.name = str(name)
        self.intelligence = intelligence
        self.strength = strength
        self.speed = speed
        self.durability = durability
        self.power = power
        self.combat = combat
        self.height = height
        self.weight = weight
